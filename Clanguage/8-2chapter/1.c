#include <stdio.h>

int main()
{
	int a, b;

	a=3;		// 00000000		00000000	00000000	00000011
	b=11;		// 00000000		00000000	00000000	00001011

	printf("%d\n",a&b); //00000011 (3)
	printf("%d\n",a|b); //00001011 (11)
	printf("%d\n",a^b); //00001000 (8)
	printf("%d\n",~a); //11111100 (-4)
	printf("%d\n",~b); //11110100 (-12)

	return 0;
}
 
