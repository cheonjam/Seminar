#include <stdio.h>

int main()
{
	int a,b;

	a=5;  // 00000000 00000000 00000000 00000101
	b=~a; // 11111111 11111111 11111111 11111010(-6);

	printf("%d\n",a<<1); // 00001010(10)
	printf("%d\n",a<<2); // 00010100(20)
	printf("%d\n",a<<3); // 00101000(40)

	printf("%d\n",b<<1); // 11110100(-12)
	printf("%d\n",b<<2); // 11101000(-24)
	printf("%d\n",b<<3); // 11010000(-48)
	
	printf("%d\n",b>>1); // 11111101(-3)
	printf("%d\n",b>>2); // 11111110(-2)
	printf("%d\n",b>>3); // 11111111(-1)

	printf("%d\n",a>>1); //	00000010(2)
	printf("%d\n",a>>2); // 00000001(1)
	printf("%d\n",a>>3); // 00000000(0)

}
	
